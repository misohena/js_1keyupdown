function drawText(ctx, text, x, y, w, h)
{
    var ti;
    for(ti = 0; ti < text.length; ti++){
        var strokes = font[text[ti]];
        if(strokes){
            
            for(si = 0; si < strokes.length; si++){
                var points = strokes[si];
                if(points.length < 4){
                    continue;
                }

                ctx.beginPath();
                ctx.moveTo(
                        points[0] * w / 256 + x,
                        points[1] * h / 256 + y);

                for(pi = 2; pi < points.length - 1; pi += 2){
                    ctx.lineTo(
                        points[pi+0] * w / 256 + x,
                        points[pi+1] * h / 256 + y);
                }

                ctx.stroke();
            }
            
        }
        x += w;
    }
    
}
